package collections;

import java.util.HashSet;
import java.util.Set;

public class hash_list {
    public static void main(String[] args) {

        Set<String> hs = new HashSet<String>();
        hs.add("mango");
        hs.add("apple");
        hs.add("grapes");

        System.out.println(hs);
    }
}
